class Product {
	long itemId;
	int price;
	String title;
	int unitPiece;
	int unitPack;
	float unitPrice;

	public Product(long itemId, int price, String title) {
		this.itemId = itemId;
		this.price = price;
		this.title = title;
	}

	public long getItemId() {
		return itemId;
	}

	public void setItemId(long itemId) {
		this.itemId = itemId;
	}

	public int getPrice() {
		return price;
	}

	public void setPrice(int price) {
		this.price = price;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}
}
